public class FileTyp {
	
	private String name;
	private String extens;
	private String abspath;
	private int level;
	
	public String getName() {
		return name;
	}
	public String getExtens() {
		return extens;
	}
	public String getAbspath() {
		return abspath;
	}
	public int getLevel() {
		return level;
	}
	
	public void setName(String val) {
		this.name = val;
	}
	public void setExtens(String val) {
		this.extens = val;
	}
	public void setAbspath(String val) {
		this.abspath = val;
	}
	public void setLevel(int val) {
		this.level = val;
	}

}
